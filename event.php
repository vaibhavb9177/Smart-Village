	<h3> &nbsp &nbsp&nbsp &nbsp&nbsp &nbsp    Datta Jayanti, also known as Dattatreya Jayanti, is a Hindu holy day, commemorating the birth day celebration of the Hindu god Dattatreya (Datta), a combined form of the Hindu male divine trinity of Brahma, Vishnu and Shiva.<br>

   &nbsp &nbsp&nbsp &nbsp&nbsp &nbsp  It is celebrated on the full moon day of the Margashirsha (Agrahayana) month according to the Hindu Calendar (December/January) throughout the country, and in particular in Maharashtra <br>

   &nbsp &nbsp&nbsp &nbsp&nbsp &nbsp  On Datta Jayanti, people take bath early in the morning in holy rivers or streams, and observe fast. A puja of Dattatreya is performed with flowers, incense, lamps and camphor. Devotees mediate on his image and pray to Dattatreya with a vow to follow in his footsteps. They remember Dattatreya's work and read the sacred books Avadhuta Gita and Jivanmukta Gita, which contain the god's discourse. Other sacred texts like the Datta Prabodh (1860) by Kavadi Baba and the Datta Mahatmya by Param Pujya Vasudevananda Saraswati (Tembe Swami Maharaj), both of which are based on Dattatreya's life, as well as the Guru-charita based on the life of Narasimha Saraswati (1378−1458), considered an avatar of Dattatreya, are read by devotees. Bhajans (devotional songs) are also sung on this day.<br>


Datta Jayanti is celebrated with much fanfare in the god's temples. The temples dedicated to Dattatreya are located throughout India, the most important places of his worship are in Karnataka, Maharashtra, Andhra Pradesh and Gujarat like Ganagapur in Karnataka near Gulbarga, Narasimha Wadi in the Kolhapur district, Pithapuram in Andhra Pradesh near Kakinada, Audumbar in Sangli district and Girnar in Saurashtra.<br><br>

