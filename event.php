	<h3> &nbsp &nbsp&nbsp &nbsp&nbsp &nbsp   Ganesha Chaturthi is the Hindu festival held on the birthday of the lord Ganesha. He was the son of Shiva and Parvati. it is celebrated all over India. The festival is held on shukla chaturthi in the Hindu month of Bhaadrapada and ends on Anant chaturdashi. For example in 2014 it will be celebrated on August 29th.It is celebrated widely in maharashtra.On this occasion women make special "modaks" which are loved by Lord Ganesha. Lalbaug is a place that every year celebrates ganesh chaturthi on a large scale. There are ganesha's form like Bal Ganesh, Lalbaugchya Raja, Siddhivinayak Maharaj and Dhagru Sheth. Out of these, most popular are Dhagru sheth and Lalbaugchya Raja.<br>

&nbsp &nbsp&nbsp &nbsp&nbsp &nbsp    Ganesh Chaturthi is also called as Vinayaka Chaturthi, it is a pure Hindu festival, during this festival people pray Lord Ganesha with great devotion. It is a ten day festival. In this occasion people keep Ganesha’s clay idol privately in homes, or publicly on big pandals .<br>

&nbsp &nbsp&nbsp &nbsp&nbsp &nbsp    Ganesha Chaturthi starts with Vedic hymns, prayers, vrata and hindu texts such as Ganesha Upanishad. Offerings of prasada after the prayer and distributing amongst the community people, prasad include sweets, modaka . This festival starts on the fourth day of Hindu luni-solar calendar month Bhadrapada, which normally falls in the month of August or September. The Ganesha chaturthi ends on the tenth day<br></h3>
