 <h3> &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp    Kabaddi is a contact team sport. Played between two teams of seven players, the objective of the game is for a single player on offence, referred to as a "raider", to run into the opposing team's half of a court, tag out as many of their defenders as possible, and return to their own half of the court, all without being tackled by the defenders, and in a single breath. Points are scored for each player tagged by the raider, while the opposing team earns a point for stopping the raider. Players are taken out of the game if they are tagged or tackled, but can be "revived" for each point scored by their team from a tag or tackle.<br>
    &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp    The exact origins of Kabaddi are disputed, with theories suggesting that Kabaddi originated from either the Vedic period of Indian history, or the Sistan region of present-day Iran. The game was said to have been popular among the Yadava people, an abhang by Tukaram stated that the god Krishna played the game in his youth, while the Mahabharata contains an account of Arjuna being able to sneak into hostile areas and take out enemies unscathedâ€”which parallels the gameplay of kabaddi. There are also accounts of Gautama Buddha having played the game recreationally. Despite these conflicting claims, India has been credited with having helped to popularize Kabaddi as a competitive sport, with the first organized competitions occurring in the 1920's, their introduction to the programme of the Indian Olympic Games in 1938, the establishment of the All-India Kabaddi Federation in 1950, and being played as a demonstration sport at the inaugural 1951 Asian Games in New Delhi. These developments helped to formalize the sport, which had traditionally been played in villages, for legitimate international competition.</h3>

